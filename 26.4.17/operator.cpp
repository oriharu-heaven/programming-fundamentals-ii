#include <iostream>
using namespace std;

int main(){
    int x = 5;
    int y = 2;
    
    cout << "加算: " << x + y << endl;
    cout << "減算: " << x - y << endl;
    cout << "乗算: " << x * y << endl;
    cout << "除算: " << x / y << endl;
    cout << "剰余: " << x % y << endl;
    cout << "インクリメント: " << x++ << endl;
    cout << "インクリメント後の値: " << x << endl;
    
    return 0;
    
}
